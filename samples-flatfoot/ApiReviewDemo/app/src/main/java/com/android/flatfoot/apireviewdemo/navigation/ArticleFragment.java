package com.android.flatfoot.apireviewdemo.navigation;

import android.os.Bundle;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import com.android.flatfoot.apireviewdemo.R;

/**
 * Fragment which shows the details of a single article
 */
public class ArticleFragment extends Fragment {
    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container,
            @Nullable Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.article_fragment, container, false);
        TextView articleNumber = (TextView) view.findViewById(R.id.article_number);
        articleNumber.setText("Article " + getArguments().getInt("articleId", -1));
        return view;
    }
}
